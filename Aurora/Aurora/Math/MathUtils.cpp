#include <Aurora/Math/MathUtils.h>
#include <math.h>

namespace Aurora
{
	namespace Math
	{
		float ASinf(float s)
		{
			return sinf(s);
		}

		float AASinf(float s)
		{
			return asinf(s);
		}

		float ACosf(float s)
		{
			return cosf(s);
		}

		float AACosf(float s)
		{
			return acosf(s);
		}

		float ASqrtf(float s)
		{
			return sqrtf(s);
		}

		float ACeilf(float s)
		{
			return ceilf(s);
		}

		float AFloorf(float s)
		{
			return floorf(s);
		}
	}
}
